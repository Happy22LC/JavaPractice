package loops;

public class ForLoop {
    public static void main(String[] args)
    {

        for(int b = 1; b <= 30; b+=2) //print the odd number
        {
            System.out.println(b);
        }

        for(int i = 2; i <= 30; i+=2) //print the even number
        {
            System.out.println(i);
        }





    }

}
